#include <stdio.h>

/** \brief Calcula la suma de 2 numeros flotantes
 *
 * \param float Valor flotante ingresado para calcular la suma
 * \param float Valor flotante ingresado para calcular la suma
 * \return float El resultado de la suma de 2 numeros flotantes
 *
 */
